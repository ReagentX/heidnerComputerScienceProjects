public class ForLoopPractice
{
    public static void main (String args[] ){
        for (int i = 7; i <= 9; i++){
            System.out.println(i + " - " + (i-1));
        }
        
        for (int asdf = 8; asdf > 2; asdf--){
            System.out.println(asdf + " - " + (asdf-1));
        }
        
        for (int math = 788; math > 5 ; math--){
            System.out.println(math);
        }
        
        for (double jack = 45.2; jack > 8; jack--){
            System.out.println("There are " + jack + " people in this class.");
        }
        
        for (int kick = 78; kick >= 90 ; kick+=6){
            System.out.println(kick);
        }
                
        for (int b = 1; b > 9; b+=6){
            System.out.println(b + " - " + (b-1));
        }
        
        for (int rack = 8; rack > 2; rack--){
            System.out.println(rack + " - " + (rack-8));
        }
        
        for (int keck = 45; keck < 0 ; keck-=9){
            System.out.println(keck);
        }
        
        for (double screws = 45.2; screws > 58.3 ; screws-=0.33){
            System.out.println("There are " + screws + " screws in this box.");
        }
        
        for (int joesss = 45; joesss <= 5; joesss-=6){
            System.out.println(joesss);
        }
    }
}
